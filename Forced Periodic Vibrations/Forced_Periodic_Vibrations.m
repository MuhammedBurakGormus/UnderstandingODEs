%FORCED PERIODIC VIBRATIONS%
%EXAMPLE1%
f = @(t,y) Example1(t,y);
tf = 16;
y0 = [2 3];
[t,y] = ode45(f,[0 tf],y0);
figure;
subplot(2,1,1);
plot(t,y(:,1));
xlabel("t");
ylabel("y(t)");
title("Example1");
grid on;
