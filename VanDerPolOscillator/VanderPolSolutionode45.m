%SOLVING SECOND ORDER DIFFERENTIAL EQUATIONS - VAN DER POL OSCILLATOR %
%clearing%
clc;
clear;
close all;
%defining mu = 1 %
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%,
%solving when mu=1%
mu = 1;
f = @(t,y) VanDerPol(t,y,mu);
tf = 50;
y0 = [0 1];
[t,y] = ode45(f, [0 tf], y0);
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
figure;
subplot(4,1,1);
plot(t,y);
xlabel("t");
ylabel("y(t)");
title("mu=1");
grid on;
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
subplot(4,1,2);
plot(y(:,1), y(:,2));
xlabel("y_1");
ylabel("y_2");
title("mu=1");
grid on;
axis equal;
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%solving when mu=0%
mu = 0;
f = @(t,y) VanDerPol(t,y,mu);
[t,y] = ode45(f, [0 tf], y0);
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
subplot(4,1,3);
plot(t,y);
xlabel("t");
ylabel("y(t)");
title("mu=0");
grid on;
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
subplot(4,1,4);
plot(y(:,1), y(:,2));
xlabel("y_1");
ylabel("y_2");
title("mu=0");
grid on;
axis equal;







